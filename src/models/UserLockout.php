class UserLockout extends AmosRecordAudit
{
    const ACCESSS_ATTEMPTS_LOCKOUT_DEFAULT = 5;
    const ACCESSS_TIME_LOCKOUT_DEFAULT = 24; // Hours

    /**
     * @inheritdoc
     */
    public static function tableName()
    {
        return 'user_lockout';
    }

    /**
     * @inheritdoc
     */
    public function rules()
