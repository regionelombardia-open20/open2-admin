namespace open20\amos\admin\assets;

use yii\web\AssetBundle;

/**
 * Class ModuleUserProfileAsset
 * @package open20\amos\admin\assets
 */
class ModuleUserProfileAsset extends AssetBundle
{
    public $sourcePath = '@vendor/open20/amos-admin/src/assets/web';

    public $css = [
        'less/user-profile.less'
    ];
    public $js = [
        'js/userprofile-js.js'
    ];
    public $depends = [
    ];

    public function init()
    {
        $moduleL = \Yii::$app->getModule('layout');
        if(!empty($moduleL))
        { $this->depends [] = 'open20\amos\layout\assets\BaseAsset'; }
        else
        { $this->depends [] = 'open20\amos\core\views\assets\AmosCoreAsset'; }
        parent::init(); // TODO: Change the autogenerated stub
    }

}
